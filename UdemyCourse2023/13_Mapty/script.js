//k√©sz√≠t√ºnk egy f≈ë oszt√°lyt a workout-oknak, majd k√©sz√≠t√ºnk 2 gyernek oszt√°lyt a running-nak √©s a cycling-nak amiket kiterjeszt√ºnk a workout oszt√°llyal
class Workout {
  date = new Date();

  //id gener√°l√°sa d√°tumb√≥l, amit els≈ënek dtring-√© alak√≠tunk majd a d√°tum utols√≥ 10 sz√°mjegy√©t haszn√°ljuk csak fel
  id = (Date.now() + '').slice(-10);
  clicks = 0;

  constructor(coords, distance, duration) {
    // this.date = ...
    // this.id = ...
    this.coords = coords; // [lat, lng]
    this.distance = distance; // in km
    this.duration = duration; // in min
  }

  _setDescription() {
    // prettier-ignore
    const months = ['January', 'February', 'March', 'April', 'May', 'June', 'July', 'August', 'September', 'October', 'November', 'December'];

    this.description = `${this.type[0].toUpperCase()}${this.type.slice(1)} on ${
      months[this.date.getMonth()]
    } ${this.date.getDate()}`;
  }

  click() {
    this.clicks++;
  }
}

class Running extends Workout {
  type = 'running';

  constructor(coords, distance, duration, cadence) {
    super(coords, distance, duration);
    this.cadence = cadence;

    //a 2 met√≥dust a konstruktorban h√≠vjuk meg, mivel a p√©ld√°ny l√©trehoz√°sakor a constructor azonnal megh√≠v√≥dik √©s az √©rt√©ket azonnal megkapjuk
    this.calcPace();
    this._setDescription();
  }

  //pace kalkul√°tor met√≥dus
  calcPace() {
    // min/km
    this.pace = this.duration / this.distance;
    return this.pace;
  }
}

class Cycling extends Workout {
  type = 'cycling';

  constructor(coords, distance, duration, elevationGain) {
    super(coords, distance, duration);
    this.elevationGain = elevationGain;
    // this.type = 'cycling';

    //a 2 met√≥dust a konstruktorban h√≠vjuk meg, mivel a p√©ld√°ny l√©trehoz√°sakor a constructor azonnal megh√≠v√≥dik √©s az √©rt√©ket azonnal megkapjuk
    this.calcSpeed();
    this._setDescription();
  }

  //speed kalkul√°tor met√≥dus
  calcSpeed() {
    // km/h
    this.speed = this.distance / (this.duration / 60);
    return this.speed;
  }
}

